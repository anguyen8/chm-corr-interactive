def get_images_in_folder(folder_path):
    image_extensions = [".jpg", ".jpeg", ".png"]  # Add more extensions if needed
    images = []
    for file in os.listdir(folder_path):
        if any(file.lower().endswith(ext) for ext in image_extensions):
            images.append(os.path.join(folder_path, file))
    return images


def build_catch(image_url, N, K, searcher=searcher):
    print("Building cache for image: {}".format(image_url))
    print("N: {}, K: {}".format(N, K))

    query_embedding = QueryToEmbeddingiNat(image_url)
    scores, indices, labels = searcher.search(query_embedding, N=N)
    result_ctr = Counter(labels[0][:K]).most_common(5)

    top1_label = result_ctr[0][0]
    top_indices = []

    for a, b in zip(labels[0][:K], indices[0][:K]):
        if a == top1_label:
            top_indices.append(b)

    gallery_images = [training_folder.imgs[int(X)][0] for X in top_indices[:5]]
    predicted_labels = {id_to_bird_name[X[0]]: X[1] / K for X in result_ctr}

    # CHM Prediction
    kNN_results = (top1_label, result_ctr[0][1], gallery_images)
    support_files = [training_folder.imgs[int(X)][0] for X in indices[0]]
    support_labels = [training_folder.imgs[int(X)][1] for X in indices[0]]

    support = [support_files, support_labels]
    # cachce CHM results for customized prediction
    chm_results = chm_cache_results(
        image_url, kNN_results, support, training_folder, N=N, K=K
    )

    # make a pickle file in a temp directory and return the path
    temp_file = tempfile.NamedTemporaryFile(delete=False)
    print(f"storing file in: {temp_file.name}")

    with open(temp_file.name, "wb") as f:
        pickle.dump(chm_results, f)

    return kNN_results, temp_file.name


def precompute(image_url, N, K, image_preprocess_mode):
    target_image_path = ""

    if image_preprocess_mode == "Normal":
        target_image_path = image_url
    elif image_preprocess_mode == "Stretch":
        # get a temp file name
        temp_file = tempfile.NamedTemporaryFile(suffix=".jpeg", delete=False)

        image_size = Image.open(image_url).convert("RGB").size
        max_dim = max(image_size)

        strech_resize = transforms.Compose(
            transforms=[transforms.Resize((max_dim, max_dim))]
        )

        strech_resize(Image.open(image_url).convert("RGB")).save(temp_file.name)

        target_image_path = temp_file.name

    elif image_preprocess_mode == "Pad":
        temp_file = tempfile.NamedTemporaryFile(suffix=".jpeg", delete=False)

        image_size = Image.open(image_url).convert("RGB").size
        max_dim = max(image_size)

        padding_resize = transforms.Compose(
            [ZeroPadToSize(target_size=(max_dim, max_dim))]
        )
        padding_resize(Image.open(image_url).convert("RGB")).save(temp_file.name)

        target_image_path = temp_file.name

    else:
        raise ValueError("Invalid image_preprocess_mode")

    N = int(N)
    K = int(K)
    preprocessed_image = preprocess(Image.open(target_image_path).convert("RGB"))
    kNN_results, chm_results = build_catch(target_image_path, N=N, K=K)
    print(kNN_results)
    return kNN_results, chm_results, preprocessed_image, "Preprocessing Done"


